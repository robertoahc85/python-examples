# Desafío Python: Análisis de Inventario de una Tienda

# 1. Crea un diccionario llamado 'inventario' con 5 productos. Cada producto debe tener como clave 
#    su nombre y como valor otro diccionario con: 'precio' (número), 'cantidad' (número) y 
#    'en_oferta' (booleano).

# 2. Crea una variable 'total_productos' que sume la cantidad de todos los productos.

# 3. Crea una variable 'valor_inventario' que calcule el valor total del inventario 
#    (precio * cantidad para cada producto).

# 4. Crea una lista 'productos_oferta' con los nombres de los productos en oferta.

# 5. Crea una variable 'producto_mas_caro' con el nombre del producto de mayor precio.

# 6. Crea una variable 'producto_mas_stock' con el nombre del producto con mayor cantidad en stock.

# 7. Crea una variable booleana 'hay_poco_stock' que sea True si algún producto tiene menos de 5 unidades.

# 8. Crea una cadena 'reporte' que contenga la siguiente información:
#    "Total de productos: [total_productos]
#     Valor del inventario: $[valor_inventario]
#     Productos en oferta: [productos_oferta]
#     Producto más caro: [producto_mas_caro]
#     Producto con más stock: [producto_mas_stock]
#     Hay productos con poco stock: [hay_poco_stock]"

# 9. Imprime la variable 'reporte'.

