# Ejemplo de uso de while para contar hasta 5
contador = 1

while contador <= 5:
    print(f"Contador: {contador}")
    contador += 1

# Ejemplo de while-else
contador = 1

while contador <= 5:
    print(f"Contador: {contador}")
    contador += 1
else:
    print("El bucle ha terminado.")

