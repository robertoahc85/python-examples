# Números Enteros (int)

edad = 25
cantidad = -10
print(type(edad))  # Output: <class 'int'>
# Números Flotantes (float)

altura = 1.75
precio = -99.99
print(type(altura))  # Output: <class 'float'>


# Números Complejos (complex)
numero_complejo = 3 + 5j
print(type(numero_complejo))  # Output: <class 'complex'>

# Cadenas de Texto (str)
nombre = "Juan"
saludo = 'Hola, ¿cómo estás?'
print(type(nombre))  # Output: <class 'str'>
# Booleanos (bool)
es_mayor_de_edad = True
tiene_permiso = False
print(type(es_mayor_de_edad))  # Output: <class 'bool'>

